package info;

/**
 * @author Chenchen Cheng
 *
 */
public class EmploymentRecord {
	private String company = "";
	private String startOfEmployment = "";
	private String employmentRecordId = "";
	private String endOfEmployment = "";
	private String department = "";
	private String position = "";
	private String salary = "";
	private String jobDesc = "";
	private String jobType = "";
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getStartOfEmployment() {
		return startOfEmployment;
	}
	public void setStartOfEmployment(String startOfEmployment) {
		this.startOfEmployment = startOfEmployment;
	}
	public String getEmploymentRecordId() {
		return employmentRecordId;
	}
	public void setEmploymentRecordId(String employmentRecordId) {
		this.employmentRecordId = employmentRecordId;
	}
	public String getEndOfEmployment() {
		return endOfEmployment;
	}
	public void setEndOfEmployment(String endOfEmployment) {
		this.endOfEmployment = endOfEmployment;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getSalary() {
		return salary;
	}
	public void setSalary(String salary) {
		this.salary = salary;
	}
	public String getJobDesc() {
		return jobDesc;
	}
	public void setJobDesc(String jobDesc) {
		this.jobDesc = jobDesc;
	}
	public String getJobType() {
		return jobType;
	}
	public void setJobType(String jobType) {
		this.jobType = jobType;
	}
	
}
